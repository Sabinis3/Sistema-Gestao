package DTO;
public class Sales {
    
    int id_sell, cod_product, cod_customer, qtd;

    public int getId_sell() {
        return id_sell;
    }

    public void setId_sell(int id_sell) {
        this.id_sell = id_sell;
    }

    public int getCod_product() {
        return cod_product;
    }

    public void setCod_product(int cod_product) {
        this.cod_product = cod_product;
    }

    public int getCod_customer() {
        return cod_customer;
    }

    public void setCod_customer(int cod_customer) {
        this.cod_customer = cod_customer;
    }

    public int getQtd() {
        return qtd;
    }

    public void setQtd(int qtd) {
        this.qtd = qtd;
    }
    
}
