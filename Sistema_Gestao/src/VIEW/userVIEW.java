    private void verificar() {
        try {
            String name, type, password;

            UserDTO objuserdto = new UserDTO();

            name = txtname.getText();
            password = txtpassword.getText();
            type = (comb_type.getSelectedItem()).toString();

            objuserdto.setName(name);
            objuserdto.setPassword(password);
            objuserdto.setType_user(type);

            userDAO objuserdao = new userDAO();
            ResultSet resultuser = objuserdao.verificar(objuserdto);
            
            if(resultuser.next()){
                JOptionPane.showMessageDialog(null, "Login Correto");
            
            }else{
            JOptionPane.showMessageDialog(null, "Login Incorreto");
            }

        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "userVIEW" + e);
        }

    }

